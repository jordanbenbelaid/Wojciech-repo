
public class Calculator {

	public double sum;

	public double add(double x, double y) {
		sum = x + y;
		return sum;
	}

	public double subtraction(double x, double y) {
		sum = x - y;
		return sum;
	}

	public double mulitplication(double x, double y) {
		sum = x * y;
		return sum;
	}

	public double divide(double x, double y) {
		sum = x / y;
		return sum;
	}
}
